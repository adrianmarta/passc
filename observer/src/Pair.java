public class Pair {
    private int contor;
    private int suma;

    public Pair(){}

    public void setContor(int contor)
    {
        this.contor=contor;
    }
    public void setSuma(int suma)
    {
        this.suma=suma;
    }
    public int getContor(){
        return contor;
    }
    public int getSuma(){
        return suma;
    }
}
